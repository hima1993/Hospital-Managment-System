    private void attd_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_attd_tableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) attd_table.getModel();
        DateFormat fmt = new SimpleDateFormat("yyyy-MM-dd");
        int i = attd_table.getSelectedRow();
        if (i >= 0) {
            try {
                attd_emp_id.setText(model.getValueAt(i, 0).toString());
                Date date = fmt.parse(model.getValueAt(i, 1).toString());
                attd_date.setDate(date);

            } catch (ParseException ex) {
                Logger.getLogger(Attendance.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_attd_tableMouseClicked

