    private void attd_emp_idKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_attd_emp_idKeyPressed
        // TODO add your handling code here:
        int key = evt.getExtendedKeyCode();
        if (key >= evt.VK_0 && key <= evt.VK_9 || key == KeyEvent.VK_BACK_SPACE || key == KeyEvent.VK_DELETE || key >= evt.VK_A && key <= evt.VK_Z) {
            attd_emp_id.setEditable(true);

        } else {
            attd_emp_id.setEditable(false);

        }

    }//GEN-LAST:event_attd_emp_idKeyPressed

